<h2>Le projet</h2>
<p class="lead">Pour un mieux-être collectif, nos villes doivent devenir intelligentes et mobiliser leurs citoyens autour de gestes éco-responsables.</p>
<p class="lead">C’est pourquoi l’entreprise PIC’VERRE se lance et souhaite proposer aux habitants de Bordeaux un Service d’Aide au Recyclage du Verre à domicile, sous la forme d’une prestation de service par abonnement.</p>

<div class="alert rounded p-3 my-4 text-center">
	<p class="m-0 font-weight-bold">Les 3 principes de PIC’VERRE sont <strong class="text-nowrap">l’écologie, l’insertion et l’innovation</strong>.</p>
</div>

<p>En étudiant la route des emballages en verre, un matériau recyclable à l’infini, il apparaît que leur recyclage reste imparfait et améliorable.</p>
<p>Le verre est encombrant et lourd et une partie de la population a du mal à le recycler. Le geste de tri n’est malheureusement pas systématique pour tous ; bien que sensibilisés, l’accès aux Points d’Apport Volontaire (PAV) est impossible pour certains ou trop contraignant pour d’autres.</p>

