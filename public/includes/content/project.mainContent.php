<p>Participant de l’Economie Sociale et Solidaire et ayant pour vocation l’insertion professionnelle, la start-up PIC’VERRE propose un service de collecte du verre au porte à porte dans les centres urbains à l’aide de tricycles à assistance électrique, munis d’une remorque adaptée et insonorisée. </p>