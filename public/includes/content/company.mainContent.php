					<p class="lead">Participant de l'<strong>Économie Sociale et Solidaire</strong>, Pic’Verre est un acteur de la croissance verte pour la transition écologique et veut responsabiliser au recyclage, développer le réemploi des emballages en verre et participer avec vous à la protection de l’environnement.</p>
					<p>Pour un mieux-être collectif, nos villes doivent devenir intelligentes et mobiliser leurs citoyens autour de gestes éco-responsables. Le tri des déchets n’a pas qu’un intérêt écologique évident, il représente aussi des enjeux économiques et politiques. Malgré les infrastructures existantes, les incitations, le cycle du verre peut encore être fluidifié et davantage optimisé.</p>	
