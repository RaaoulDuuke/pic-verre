			<div class="row ">
				<div class="col">
					<p class="lead"><strong>Les 3 objectifs de Pic’Verre sont :<br/>&bull; l’écologie par le recyclage et le réemploi des emballages en verre<br>&bull; la création d’un circuit court<br>&bull; l’emploi par l’insertion</strong></p>
					<p>Bien que sensibilisés, l’accès aux Points d’Apport Volontaire est impossible pour certains ou trop contraignant pour d’autres. Ne pouvant pas nous substituer aux services publics existants, notre action est d’aider au recyclage du verre des particuliers cette matière noble <strong>recyclable à 100% et à l’infini</strong>. Car malheureusement le verre est encombrant, lourd et une partie de la population a du mal à faire le geste de tri. </p>
					<p>Pic’verre en ouvrant son service aux professionnels, secteur concurrentiel, se lance dans la création d’un centre de tri et de lavage pour le réemploi des emballages verre. Grâce à notre logistique de collecte, nous souhaitons développer un circuit court, entouré de nos partenaires locaux et faciliter le retour de la consigne sur la région.</p>
