<h3>Fonctionnement</h3>
<p><strong>Nous collectons du lundi au vendredi entre 10h&nbsp;et&nbsp;12h30</strong> à l'aide de tricycles à assistance électrique équipés d'une remorque.</p>
<p><strong>Vous choisissez le jour de votre collecte au plus tard la veille pour le lendemain</strong>, depuis notre site internet ou notre application.</p>
<p><strong>Le jour de la collecte vous remettez au picker les sacs de verre à recycler</strong> et il vous restituera le même nombre de sacs pour votre prochaine collecte.</p>

<h3>Ouverture d'un compte</h3>
<p>Afin de faciliter le stockage et optimiser le temps de collecte, <strong>vous allez devoir faire l'acquisition du nombre de sacs que le picker devra recupérer lors de chaque collecte, au moment de l'ouverture de votre compte uniquement</strong>.</p>
<p>Pour la flexibilité du service <strong>vous devrez également constituer un compte de crédits correspondant à vos besoins : <span class="text-nowrap">1 crédit = 1 sac collecté</span> soit 5kg de verre environ</strong>.</p>
<p><strong>Contactez nous au 07 67 75 02 12 pour une étude personalisée de vos besoins</strong>, nous pouvons également nous déplacer dans vos locaux.</p>
	

